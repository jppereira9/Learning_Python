# Adicionando elementos na lista

L = [] # Lista L (vazia)
print(L, len(L)) #Lista vazia

L.append("a") # Adicionando a
print(L) # Imprime "a"

L.append("b") # Adicionando b
print(L) # Imprime a, b

L.append("c") # Adicionando c
print(L) # Imprime a, b, c

print(len(L)) # Imprime o tamanho da lista (3)