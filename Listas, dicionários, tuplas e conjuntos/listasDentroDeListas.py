'''
Um fator interessante é que podemos acessar as strings dentro da lista, letra por letra, usando
um segundo índice

'''
s = ["Maçãs", "Peras", "Kiwis"] # Lista com as frutas

print(s[0][0]) # Imprime a primeira letra da primeira palavra
print(s[0][1]) # Imprime a segunda letra da primeira palavra
print(s[1][1]) # Imprime a segunda letra da segunda palavra
print(s[2][2]) #Imprime a terceira letra da terceira palavra