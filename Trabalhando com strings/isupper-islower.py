'''
Podemos também verificar se todos os carateres de uma string são letras maiúsculas ou minúsculas usando
isupper e islower respectivamente:
'''
s = "ABC"
p = "abc"
e = "aBc"
print(f'{s.isupper()}')
print(f'{s.islower()}')
print(f'{p.isupper()}')
print(f'{p.islower()}')
print(f'{e.isupper()}')
print(f'{e.islower()}')
