# Exercicío 5.3 -  Faça um programa para escrever a contagem regressiva do lancamento de um foguete. O programa deve imprimir 10, 9, 8 ... 1,0 e Fogo! na tela.

x = 10 # x recebe 10
while x >= 0 : # Enquanto x for maior ou igual a 0, faça
    print(x) # Imprime o valor de x
    x = x - 1 # x é decrementado
print("\nWe have liftoff") # Decolagem